document.getElementById('toggleViewTableOrderItems').addEventListener('click', function() {
    var form = document.getElementById('tableOrderItems');
    if (form.style.display === 'none' || form.style.display === '') {
        form.style.display = 'block';
        form.scrollIntoView({ behavior: 'smooth' });
    } else {
        form.style.display = 'none';
    }
});